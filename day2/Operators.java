package week1.day2;

public class Operators {
    public static void main(String[] args) {

        // Declare and initialize maxRange
        int maxRange = 10;

        // Print odd numbers from 1 to 10
        for (int i = 1; i <= maxRange; i++) {

            // Check if the number is odd
            if (i % 2 == 1) {
                System.out.println(i);
            }
        }
    }
}
